    def _prompt_vault_passphrase(self, *, confirm: bool, setup_flow: bool = False) -> Optional[str]:
        title = self.local_text("Set Vault Passphrase", "设置 Vault 密码") if setup_flow else self.local_text("Vault Passphrase", "Vault 密码")
        prompt = self.local_text("Set the vault passphrase.", "请设置 Vault 密码。") if setup_flow else self.local_text("Enter the vault passphrase.", "请输入 Vault 密码。")
        first = simpledialog.askstring(
            title,
            prompt,
            parent=self.root,
            show="*",
        )
        if first is None:
            return None
        first = first.strip()
        if not first:
            messagebox.showwarning(
                self.local_text("Vault Passphrase", "Vault 密码"),
                self.local_text("Vault passphrase cannot be empty.", "Vault 密码不能为空。"),
                parent=self.root,
            )
            return None
        if not confirm:
            return first
        second = simpledialog.askstring(
            self.local_text("Confirm Vault Passphrase", "确认 Vault 密码"),
            self.local_text("Re-enter the vault passphrase.", "请再次输入 Vault 密码。"),
            parent=self.root,
            show="*",
        )
        if second is None:
            return None
        if first != second.strip():
            messagebox.showerror(
                self.local_text("Vault Passphrase", "Vault 密码"),
                self.local_text("Vault passphrase confirmation did not match.", "两次输入的 Vault 密码不一致。"),
                parent=self.root,
            )
            return None
        return first

    def manage_vault(self) -> None:
        try:
            status = vault_status()
            state = str(status.get("state") or "unknown")
            mode = str(status.get("mode") or "manual_once")
            if state == "uninitialized":
                passphrase = self._prompt_vault_passphrase(confirm=True, setup_flow=True)
                if passphrase is None:
                    return
                setup_linux_vault(mode="manual_once", passphrase=passphrase, unlock=True)
                messagebox.showinfo(
                    self.local_text("Vault Ready", "Vault 已就绪"),
                    self.local_text("The application vault has been initialized and unlocked.", "应用层 Vault 已初始化并解锁。"),
                    parent=self.root,
                )
            elif state == "unlocked":
                lock_linux_vault()
            else:
                passphrase = self._prompt_vault_passphrase(confirm=False)
                if passphrase is None:
                    return
                unlock_linux_vault(passphrase)
        except ProfileError as exc:
            messagebox.showerror(
                self.local_text("Vault Error", "Vault 错误"),
                str(exc),
                parent=self.root,
            )
            return
        self.refresh_vault_status()
        self.refresh_profiles()
        if self.current_profile_id:
            profile = get_profile_by_id(self.current_profile_id)
            if profile is not None:
                self._update_profile_credential_status(profile.profile_id, profile.api_key_hint)

    def _confirm_vault_reset(self) -> bool:
        warning_title = self.local_text("Reset Vault", "重置 Vault")
        warning_body = self.local_text(
            "Resetting the vault deletes all saved profiles, the default profile, and all stored secrets. This cannot be undone.",
            "重置 Vault 会删除所有已保存账号、默认账号和全部密钥，且无法恢复。",
        )
        confirmed = messagebox.askyesno(
            warning_title,
            warning_body,
            parent=self.root,
        )
        if not confirmed:
            return False
        token = simpledialog.askstring(
            warning_title,
            self.local_text("Type RESET to continue.", "请输入 RESET 继续。"),
            parent=self.root,
        )
        if token is None:
            return False
        if token.strip().upper() != "RESET":
            messagebox.showerror(
                warning_title,
                self.local_text("Confirmation text did not match RESET.", "确认文本不匹配 RESET。"),
                parent=self.root,
            )
            return False
        return True

    def reset_vault(self) -> None:
        if not self._confirm_vault_reset():
            return
        try:
            reset_linux_vault()
        except ProfileError as exc:
            messagebox.showerror(
                self.local_text("Vault Error", "Vault 错误"),
                str(exc),
                parent=self.root,
            )
            return
        self.current_profile_id = None
        self.reset_form()
        self.refresh_vault_status()
        self.refresh_profiles()
        messagebox.showinfo(
            self.local_text("Vault Reset", "Vault 已重置"),
            self.local_text(
                "Vault reset complete. All saved profiles were removed and the vault returned to setup required.",
                "Vault 已重置。所有已保存账号已删除，保险箱已回到未初始化状态。",
            ),
            parent=self.root,
        )
        self._refresh_agent_cache("profile-manager.ui.vault-reset")

    def refresh_profiles(self) -> None:
        self.profile_rows = {}
        if self.profile_listbox is None:
            return
        self.profile_listbox.delete(0, tk.END)
        self.refresh_vault_status()
        profiles = list_profiles()
        default_profile_id = get_default_profile_id()
        default_profile = get_default_profile_name()
        for idx, profile in enumerate(profiles):
            marker = self.t("default_marker") if default_profile_id == profile.profile_id else ""
            description = f"  {profile.description}" if profile.description else ""
            hint = f" ({profile.api_key_hint})" if profile.api_key_hint else ""
            self.profile_listbox.insert(tk.END, f"{profile.name}{marker}{hint}{description}")
            self.profile_rows[idx] = profile.profile_id
        self.profile_count_var.set(str(len(profiles)))
        self.default_profile_var.set(default_profile or self.local_text("Not configured", "未设置"))
        self.backend_var.set(secure_store_backend_name())
        self._restore_profile_selection()

    def reset_form(self) -> None:
        self.current_profile_id = None
        self.name_var.set("")
        self.contract_base_url_var.set("")
        self._clear_secret_placeholders()
        self.default_var.set(False)
        self.credential_status_var.set(self.t("credential_status_unsaved"))
        self.description_buffer = ""
        if self.description_text is not None:
            self.description_text.delete("1.0", tk.END)
        if self.profile_listbox is not None:
            self.profile_listbox.selection_clear(0, tk.END)
        self.editor_context_var.set(self.local_text("Creating a new profile", "当前正在创建新账户"))
        self._set_mode_badge(editing=False)
