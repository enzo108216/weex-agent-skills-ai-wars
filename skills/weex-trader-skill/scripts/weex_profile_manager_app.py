    def _schedule_layout_refresh(self, force: bool = False) -> None:
        if self._resize_after_id is not None:
            self.root.after_cancel(self._resize_after_id)
            self._resize_after_id = None
        if force:
            self._refresh_responsive_layout(force=True)
            return
        self._resize_after_id = self.root.after(40, self._refresh_responsive_layout)

    def _refresh_responsive_layout(self, force: bool = False) -> None:
        self._resize_after_id = None
        viewport_width, viewport_height = self._current_viewport_size()
        metrics = compute_layout_metrics(
            viewport_width=viewport_width,
            viewport_height=viewport_height,
        )
        if not force and metrics == self._layout_metrics:
            return

        self._capture_description_buffer()
        self._layout_metrics = metrics
        self.form_columns = int(metrics["form_columns"])
        self.section_wraplength = int(metrics["section_wraplength"])
        self.summary_wraplength = int(metrics["summary_wraplength"])
        self.hero_wraplength = int(metrics["hero_wraplength"])
        self._apply_font_scale(float(metrics["scale"]))
        self._render_layout()
        self._restore_description_buffer()
        self.refresh_vault_status()
        self.refresh_profiles()
        self._restore_profile_selection()
        self._set_mode_badge(self.mode_badge_editing)
        self._set_credential_badge(self.credential_badge_tone, self.credential_status_var.get())

