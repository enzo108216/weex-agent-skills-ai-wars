    def _build_fonts(self) -> Dict[str, tkfont.Font]:
        available = set(tkfont.families(self.root))

        def pick(*families: str) -> str:
            for family in families:
                if family in available:
                    return family
            return "TkDefaultFont"

        body_family = pick("Microsoft YaHei UI", "Microsoft YaHei", "PingFang SC", "Segoe UI", "Helvetica Neue")
        display_family = pick("Bahnschrift", "Microsoft YaHei UI", "Segoe UI Semibold", "PingFang SC", body_family)
        mono_family = pick("Cascadia Mono", "Consolas", "JetBrains Mono", "Menlo", "Courier New")
        specs = {
            "eyebrow": (body_family, 10, "bold"),
            "hero_title": (display_family, 22, "bold"),
            "hero_body": (body_family, 11, None),
            "title": (display_family, 17, "bold"),
            "section_title": (display_family, 11, "bold"),
            "metric_label": (body_family, 9, None),
            "metric_value": (display_family, 16, "bold"),
            "label": (body_family, 10, "bold"),
            "body": (body_family, 10, None),
            "small": (body_family, 9, None),
            "button": (body_family, 10, "bold"),
            "mono": (mono_family, 9, None),
            "list": (body_family, 10, None),
            "badge": (body_family, 9, "bold"),
        }
        self._font_base_sizes = {name: size for name, (_family, size, _weight) in specs.items()}

        fonts: Dict[str, tkfont.Font] = {}
        for name, (family, size, weight) in specs.items():
            kwargs: Dict[str, object] = {"family": family, "size": size}
            if weight is not None:
                kwargs["weight"] = weight
            fonts[name] = tkfont.Font(**kwargs)
        return fonts

    def _apply_font_scale(self, scale: float) -> None:
        for name, font in self.fonts.items():
            base_size = self._font_base_sizes[name]
            minimum = max(7, int(round(base_size * MIN_LAYOUT_SCALE)))
            font.configure(size=max(minimum, int(round(base_size * scale))))

    def _apply_window_geometry(self) -> None:
        self.root.update_idletasks()
