    def on_select_profile(self, _event: object) -> None:
        if self.profile_listbox is None:
            return
        selection = self.profile_listbox.curselection()
        if not selection:
            return
        profile_id = self.profile_rows.get(selection[0])
        if not profile_id:
            return
        profile = get_profile_by_id(profile_id)
        if profile is None:
            return
        self.current_profile_id = profile.profile_id
        self.name_var.set(profile.name)
        self.contract_base_url_var.set(profile.contract_base_url)
        self.description_buffer = profile.description
        if self.description_text is not None:
            self.description_text.delete("1.0", tk.END)
            self.description_text.insert("1.0", profile.description)
        self.default_var.set(get_default_profile_id() == profile.profile_id)
        self.editor_context_var.set(self.local_text(f"Editing: {profile.name}", f"当前编辑：{profile.name}"))
        self._set_mode_badge(editing=True)
        if self._update_profile_credential_status(profile.profile_id, profile.api_key_hint):
            self._show_saved_secret_placeholders(profile.api_key_hint)
        else:
            self._clear_secret_placeholders()

    def save_profile(self) -> None:
        if not self.profile_actions_enabled:
            messagebox.showwarning(
                self.local_text("Vault Required", "需要先解锁 Vault"),
                self.local_text(
                    "Initialize or unlock the global vault control area before saving or rotating profile credentials.",
                    "请先初始化或解锁上方的全局 Vault 控制区，再保存账号或轮换密钥。",
                ),
                parent=self.root,
            )
            return
        name = self.name_var.get().strip()
        description = self.description_text.get("1.0", tk.END).strip()
        contract_base_url = self.contract_base_url_var.get().strip()
        api_key = self._secret_value_for_save("api_key")
        api_secret = self._secret_value_for_save("api_secret")
        api_passphrase = self._secret_value_for_save("api_passphrase")

        try:
            contract_base_url = self._validate_optional_base_url(
                contract_base_url,
                self.t("field_contract_base_url"),
            )
        except BaseUrlPolicyError as exc:
            messagebox.showerror(self.t("save_error_title"), exc.localized_message(self.language), parent=self.root)
            return

        try:
            profile = upsert_profile(
                name=name,
                description=description,
                contract_base_url=contract_base_url,
                api_key=api_key,
                api_secret=api_secret,
                api_passphrase=api_passphrase,
                profile_id=self.current_profile_id,
            )
            if self.default_var.get():
                set_default_profile(profile.profile_id)
            elif get_default_profile_id() == profile.profile_id:
                set_default_profile(None)
        except ProfileError as exc:
            messagebox.showerror(self.t("save_error_title"), str(exc), parent=self.root)
            return

        messagebox.showinfo(
            self.t("save_success_title"),
            self.t("save_success_message", name=profile.name),
            parent=self.root,
        )
        self.refresh_profiles()
        self.current_profile_id = profile.profile_id
        self.editor_context_var.set(self.local_text(f"Editing: {profile.name}", f"当前编辑：{profile.name}"))
        self._set_mode_badge(editing=True)
        self._show_saved_secret_placeholders(profile.api_key_hint)
        self._update_profile_credential_status(profile.profile_id, profile.api_key_hint)
        self._refresh_agent_cache("profile-manager.ui.save")

    def delete_current_profile(self) -> None:
        profile = get_profile_by_id(self.current_profile_id) if self.current_profile_id else None
        name = profile.name if profile is not None else self.name_var.get().strip()
        if not name:
            messagebox.showwarning(
                self.t("delete_warning_title"),
                self.t("delete_warning_message"),
                parent=self.root,
            )
            return
        if not self.profile_actions_enabled:
            messagebox.showwarning(
                self.local_text("Vault Required", "需要先解锁 Vault"),
                self.local_text(
                    "Initialize or unlock the global vault control area before deleting a profile.",
                    "请先初始化或解锁上方的全局 Vault 控制区，再删除账号。",
                ),
                parent=self.root,
            )
            return
        confirmed = messagebox.askyesno(
            self.t("delete_confirm_title"),
            self.t("delete_confirm_message", name=name),
            parent=self.root,
        )
        if not confirmed:
            return
        try:
            if profile is not None:
                deleted = delete_profile_by_id(profile.profile_id)
            else:
                deleted = delete_profile(name)
        except ProfileError as exc:
            messagebox.showerror(self.t("delete_error_title"), str(exc), parent=self.root)
            return
        if not deleted:
            messagebox.showwarning(
                self.t("delete_not_found_title"),
                self.t("delete_not_found_message", name=name),
                parent=self.root,
            )
            return
        messagebox.showinfo(
            self.t("delete_success_title"),
            self.t("delete_success_message", name=name),
            parent=self.root,
        )
        self.refresh_profiles()
        self.reset_form()
        self._refresh_agent_cache("profile-manager.ui.delete")

